int zhuyinToPinyin(char* buf, size_t* splStart, size_t bufSize, const wchar_t* zhuyinStr) {
    std::wstring str(zhuyinStr);

    if (bufSize == 0) {
        return 0;
    }

    if (str.length() == 0) {
        splStart[0] = 0;
        buf[0] = '\0';
        return 0;
    }

    std::string pinyinStr;

    splStart[0] = 0;
    splStart[1] = 0;

    size_t pos = 0;
    size_t splPos = 0;
    size_t hanziCount = 0;
    while (pos < str.length()) {
        size_t i;
        size_t remain = str.length() - pos;
        for (i = (remain > 4)? 4:remain; i > 0; i--) {
            std::wstring subStr = str.substr(pos, i);

            std::map<std::wstring, std::string>::const_iterator it = zhuyinToPinyinMap.find(subStr);
            if (it != zhuyinToPinyinMap.end()) {
                pinyinStr += it->second + "\'";
                splPos++;
                splStart[splPos] = pos;
                hanziCount += 1;
                break;
            }
        }

        if (i == 0) {
            // convert error
            return -1;
        }

        pos += i;
    }

    splStart[0] = splPos - 1;
    pinyinStr.copy(buf, bufSize-1);
    buf[bufSize-1] = '\0';
    if (pinyinStr.length() > 0 && pinyinStr.length() < bufSize - 1)
        buf[pinyinStr.length()] = '\0';
    return hanziCount;
}
